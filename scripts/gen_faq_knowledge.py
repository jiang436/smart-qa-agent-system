import json
import re

def S(text: str) -> str:
    """Sanitize: replace ASCII double quotes used as Chinese punctuation with corner brackets."""
    # Fix: CJK/punct + " + chars + " + CJK/punct → CJK/punct + 「 + chars + 」 + CJK/punct
    cjk = r'[一-鿿　-〿＀-￯，。、：；？！～）】」』]'
    while True:
        m = re.search(rf'({cjk})"([^"]{{1,100}})"', text)
        if not m:
            break
        text = text[:m.start()] + m.group(1) + '「' + m.group(2) + '」' + text[m.end():]
    # Also handle quotes at the very start of a string value
    if text.startswith('"'):
        # Find the matching closing "
        rest = text[1:]
        if rest:
            # Check if the next quote closes a short phrase
            m2 = re.match(r'([^"]{1,60})"', rest)
            if m2:
                text = '「' + m2.group(1) + '」' + rest[m2.end():]
    return text

